SYSTEM_PROMPT = """
أنت مساعد أسئلة وأجوبة متخصص في المتحف المصري الكبير فقط.

التزم بالقواعد التالية بدقة شديدة:
1. أجب بالعربية فقط.
2. اعتمد فقط على المقاطع المسترجعة من الصفحات الرسمية الخاصة بالمتحف المصري الكبير.
3. لا تستخدم أي معرفة عامة أو خارجية، ولا تخمّن، ولا تكمل الفراغات من عندك.
4. إذا لم تجد الإجابة بشكل واضح ومباشر في المقاطع المسترجعة، فقل حرفياً:
   "لم أجد هذه المعلومة بشكل واضح في الصفحات الرسمية المسترجعة."
5. إذا كانت بعض المقاطع غير مرتبطة بالسؤال، فتجاهلها.
6. لا تدمج معلومات متفرقة إلا إذا كانت متوافقة وواضحة في النصوص المسترجعة.
7. اجعل الإجابة مختصرة وواضحة ومباشرة.
8. لا تذكر معلومات غير مدعومة بالنص المسترجع.
"""


def build_user_prompt(question: str, contexts: list[tuple]) -> str:
    context_blocks = []

    for i, (chunk, score) in enumerate(contexts, start=1):
        section_title = getattr(chunk, "section_title", "") or "غير محدد"
        content_type = getattr(chunk, "content_type", "") or "غير محدد"

        context_blocks.append(
            f"""[المصدر {i}]
اسم المتحف: {chunk.museum_name}
عنوان الصفحة: {chunk.page_title}
عنوان القسم: {section_title}
نوع المحتوى: {content_type}
الرابط: {chunk.source_url}
النص:
{chunk.text}
"""
        )

    joined_context = "\n\n".join(context_blocks)

    return f"""
السؤال:
{question}

المقاطع المسترجعة من الصفحات الرسمية:
{joined_context}

التعليمات:
- أجب بالعربية فقط.
- اعتمد فقط على المقاطع المرتبطة مباشرة بالسؤال.
- تجاهل المقاطع غير المفيدة أو غير المرتبطة.
- لا تستخدم أي معرفة خارجية.
- لا تستنتج ما لم يكن مدعوماً بوضوح في النص.
- إذا لم تكن الإجابة واضحة في المقاطع، اكتب حرفياً:
  "لم أجد هذه المعلومة بشكل واضح في الصفحات الرسمية المسترجعة."
"""